const LaunchPlanSection = ({ num, title, plan }: { num: string; title: string; plan: LaunchPlan }) => {
  const isDesktop = typeof window !== "undefined" && window.matchMedia("(min-width: 768px)").matches;

  if (typeof plan === "string" || !plan?.phases?.length) {
    const text = typeof plan === "string" ? plan : "";
    return (
      <article className="animate-fade-in rounded-xl border border-border border-l-2 border-l-primary bg-card p-5 sm:p-6">
        <div className="mb-3 flex items-baseline gap-3">
          <span className="font-mono-num text-xs text-muted-foreground">{num}</span>
          <h2 className="text-base font-semibold text-foreground sm:text-lg">{title}</h2>
        </div>
        <p className="whitespace-pre-line text-sm leading-relaxed text-muted-foreground sm:text-[15px] sm:leading-[1.7]">
          {text}
        </p>
      </article>
    );
  }

  return (
    <article className="animate-fade-in rounded-xl border border-border border-l-2 border-l-primary bg-card p-5 sm:p-6">
      <div className="mb-4 flex items-baseline gap-3">
        <span className="font-mono-num text-xs text-muted-foreground">{num}</span>
        <h2 className="text-base font-semibold text-foreground sm:text-lg">{title}</h2>
      </div>
      <p className="mb-4 text-xs text-muted-foreground sm:text-sm">
        Five sequential phases covering the first 14 days. Tap a phase to expand.
      </p>
      <div className="space-y-3">
        {plan.phases.map((phase, i) => (
          <PhaseCard key={i} phase={phase} index={i} defaultOpen={isDesktop} />
        ))}
      </div>
    </article>
  );
};

export const ResultsView = ({ ideaName, answers, report, onStartOver }: ResultsViewProps) => {
  const [copied, setCopied] = useState(false);
  const [promptCopied, setPromptCopied] = useState(false);

  const copyText = async (text: string) => {
    try {
      await navigator.clipboard.writeText(text);
    } catch {
      const ta = document.createElement("textarea");
      ta.value = text;
      document.body.appendChild(ta);
      ta.select();
      document.execCommand("copy");
      document.body.removeChild(ta);
    }
  };

  const handleCopy = async () => {
    await copyText(buildPlainText(ideaName, report));
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleCopyPrompt = async () => {
    await copyText(buildMarketingPrompt(ideaName, answers, report));
    setPromptCopied(true);
    setTimeout(() => setPromptCopied(false), 2000);
  };

  const handleDownloadPdf = () => {
    downloadBlueprintPdf(ideaName, answers, report);
  };

  const meta = [answers.budget, answers.hours, answers.experience, answers.goal]
    .filter(Boolean)
    .join(" · ");

  return (
    <div className="min-h-screen">
      {/* Sticky action bar */}
      <div className="sticky top-0 z-20 border-b border-border bg-background/85 backdrop-blur supports-[backdrop-filter]:bg-background/70">
        <div className="mx-auto flex max-w-[820px] items-center justify-between gap-3 px-4 py-3 sm:px-6">
          <Logo size="sm" />
          <div className="flex flex-wrap items-center justify-end gap-2">
            <button
              onClick={handleDownloadPdf}
              className="rounded-lg bg-primary px-3 py-2 text-xs font-semibold text-primary-foreground shadow-[0_8px_24px_-12px_hsl(var(--primary)/0.7)] transition hover:brightness-110 sm:text-sm"
            >
              ↓ PDF
            </button>
            <button
              onClick={handleCopyPrompt}
              className="rounded-lg border border-primary/60 bg-transparent px-3 py-2 text-xs font-semibold text-primary transition hover:bg-primary/10 sm:text-sm"
            >
              {promptCopied ? "Copied ✓" : "Marketing Prompt"}
            </button>
            <button
              onClick={handleCopy}
              className="rounded-lg border border-border bg-transparent px-3 py-2 text-xs font-medium text-muted-foreground transition hover:border-primary/50 hover:text-foreground sm:text-sm"
            >
              {copied ? "Copied ✓" : "Copy Plan"}
            </button>
            <button
              onClick={onStartOver}
              className="rounded-lg px-3 py-2 text-xs font-medium text-muted-foreground transition hover:text-foreground sm:text-sm"
            >
              Start Over
            </button>
          </div>
        </div>
      </div>

      <main className="mx-auto w-full max-w-[820px] px-4 py-10 sm:px-6 sm:py-14">
        <p className="text-xs uppercase tracking-[0.16em] text-muted-foreground">
          Your SoloBlueprint for:
        </p>
        <h1 className="mt-2 font-display text-3xl text-foreground sm:text-4xl md:text-5xl">
          {ideaName}
        </h1>
        {meta && (
          <p className="mt-3 text-xs text-muted-foreground sm:text-sm">{meta}</p>
        )}

        <div className="mt-10 space-y-5">
          {SECTIONS.map((section) => {
            if (section.key === "launch_plan") {
              return (
                <LaunchPlanSection
                  key={section.num}
                  num={section.num}
                  title={section.title}
                  plan={report.launch_plan}
                />
              );
            }
            return (
              <article
                key={section.num}
                className="animate-fade-in rounded-xl border border-border border-l-2 border-l-primary bg-card p-5 sm:p-6"
              >
                <div className="mb-3 flex items-baseline gap-3">
                  <span className="font-mono-num text-xs text-muted-foreground">
                    {section.num}
                  </span>
                  <h2 className="text-base font-semibold text-foreground sm:text-lg">
                    {section.title}
                  </h2>
                </div>
                <p className="whitespace-pre-line text-sm leading-relaxed text-muted-foreground sm:text-[15px] sm:leading-[1.7]">
                  {report[section.key] as string}
                </p>
              </article>
            );
          })}
        </div>

        <footer className="mt-14 border-t border-border pt-6 text-center text-xs text-muted-foreground">
          SoloBlueprint · Built for solo founders · soloblueprint.co.uk
        </footer>
      </main>
    </div>
  );
};
